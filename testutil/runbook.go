// BenchmarkSet returns a server and a path pattern for benchmarking.
func BenchmarkSet(t testing.TB, bookCount, stepCount int, body string) (*httptest.Server, string) {
	const httpRunbookTmpl = `desc: Test using HTTP
runners:
  req: {{ .Endpoint }}
steps:
{{ range $k := .Steps }}
-
  req:
    /:
      post:
        body:
          application/json:
            {{ $.Body }}
{{ end }}
`
	ts := echoServer(t)
	t.Cleanup(func() {
		ts.Close()
	})
	dir := t.TempDir()
	t.Cleanup(func() {
		if err := os.RemoveAll(dir); err != nil {
			t.Fatal(err)
		}
	})
	tmpl, err := template.New("http").Parse(httpRunbookTmpl)
	if err != nil {
		t.Fatal(err)
	}
	for i := range bookCount {
		book := filepath.Join(dir, fmt.Sprintf("http-%d.yml", i))
		f, err := os.Create(book)
		if err != nil {
			t.Fatal(err)
		}
		if err := tmpl.Execute(f, map[string]any{
			"Endpoint": ts.URL,
			"Steps":    make([]struct{}, stepCount),
			"Body":     body,
		}); err != nil {
			t.Fatal(err)
		}
	}
	return ts, fmt.Sprintf("%s/*.yml", dir)
}

// BenchmarkSetWithOpenAPI3 returns a server and a path pattern for benchmarking.
func BenchmarkSetWithOpenAPI3(t testing.TB, bookCount, stepCount int) (*httptest.Server, string) {
	const httpRunbookTmpl = `desc: Test using HTTP
runners:
  req:
    endpoint: {{ .Endpoint }}
    openapi3: {{ .OpenAPI3 }}
steps:
{{ range $k := .Steps }}
-
  req:
    /issues:
      get:
        body:
          application/json:
            body: null
{{ end }}
`
	ts := githubServer(t)
	t.Cleanup(func() {
		ts.Close()
	})
	dir := t.TempDir()
	t.Cleanup(func() {
		if err := os.RemoveAll(dir); err != nil {
			t.Fatal(err)
		}
	})
	// Download OpenAPI3 Spec for GitHub API
	res, err := http.DefaultClient.Get("https://raw.githubusercontent.com/github/rest-api-description/main/descriptions/api.github.com/api.github.com.yaml")
	if err != nil {
		t.Fatal(err)
	}
	b, err := io.ReadAll(res.Body)
	if err != nil {
		t.Fatal(err)
	}
	res.Body.Close()
	openapi3 := filepath.Join(dir, "github-api.yaml")
	if err := os.WriteFile(openapi3, b, os.ModePerm); err != nil { //nolint:gosec
		t.Fatal(err)
	}
	tmpl, err := template.New("http").Parse(httpRunbookTmpl)
	if err != nil {
		t.Fatal(err)
	}
	for i := range bookCount {
		book := filepath.Join(dir, fmt.Sprintf("http-with-openapi3-%d.yml", i))
		f, err := os.Create(book)
		if err != nil {
			t.Fatal(err)
		}
		if err := tmpl.Execute(f, map[string]any{
			"Endpoint": ts.URL,
			"OpenAPI3": openapi3,
			"Steps":    make([]struct{}, stepCount),
		}); err != nil {
			t.Fatal(err)
		}
	}
	return ts, fmt.Sprintf("%s/*.yml", dir)
}

// echoServer returns a server that does not do anything special, just echo.
// For benchmarking.
func echoServer(t testing.TB) *httptest.Server {
	ts := httptest.NewServer(http.HandlerFunc(func(w http.ResponseWriter, r *http.Request) {
		_, _ = io.Copy(w, r.Body)
	}))
	t.Cleanup(func() {
		ts.Close()
	})
	return ts
}

